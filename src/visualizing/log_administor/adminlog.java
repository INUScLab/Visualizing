package visualizing.log_administor;

public class adminlog {
	int num;
	String dayandtime;
	String id;
	String ip;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getDayandtime() {
		return dayandtime;
	}
	public void setDayandtime(String dayandtime) {
		this.dayandtime = dayandtime;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	
	

}
