package visualizing.read;

public class ReadMonth {

	private String code;
	private String detail;
	private String number;
	private String meter_num;
	private String month;
	private String value;
	private String pre_value;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getDetail() {
		return detail;
	}

	public void setDetail(String detail) {
		this.detail = detail;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}

	public String getMeter_num() {
		return meter_num;
	}

	public void setMeter_num(String meter_num) {
		this.meter_num = meter_num;
	}

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getPre_value() {
		return pre_value;
	}

	public void setPre_value(String pre_value) {
		this.pre_value = pre_value;
	}
	
}
